<?php

    $num_1 = 13;
    $num_2 = 5;

    // Addition Operator "+
    echo $num_1 + $num_2;
    echo "<br />";

    // Substraction Operator "-"
    echo $num_1 - $num_2;
    echo "<br />";

    // Multiplication Operator "*"
    echo $num_1 * $num_2;
    echo "<br />";

    // Division Operator "/"
    echo $num_1 / $num_2;
    echo "<br />";

    // Exponent Operator "**"
    echo $num_1 ** $num_2;
    echo "<br />";

    // Modulus Operator "%"
    echo $num_1 % $num_2;

?>